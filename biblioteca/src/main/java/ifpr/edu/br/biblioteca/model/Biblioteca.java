package ifpr.edu.br.biblioteca.model;

public class Biblioteca {
    private int id;
    private String nome;
    public Biblioteca() {
    }

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
}
